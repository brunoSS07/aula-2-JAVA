public class Exemplo02 {
    public static void main(String[] args) {
        int a = 5;

        //pós-incremento
        System.out.println(a++); // usa a variável e depois soma 1
        System.out.println(a);

        a = 5;
        // pré-incremento
        System.out.println(++a); // primeiro soma 1 na variável e depois usa a variável
        System.out.println(a);

    }
}
