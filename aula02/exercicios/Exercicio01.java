package exercicios;

/* Escreva um programa para exibir oi resultado das 4 operções matematica basica (+, -, *, /) */

public class Exercicio01 {
    public static void main(String[] args) {
        System.out.println("A soma de 15 + 7 é: " + (15 + 7) );
        System.out.println("A subtração de 15 - 7 é: " + (15 - 7) );
        System.out.println("A Multiplicação de 15 * 7 é: " + (15 * 7) );
        System.out.println("A divisão de 15 / 7 é: " + (15.0 / 7) );
    }
}
